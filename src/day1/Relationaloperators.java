package day1;

public class Relationaloperators {

	public static void main(String[] args) {
		// Relational operators
		int a=10;
		int b=20;
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
				

	}

}
